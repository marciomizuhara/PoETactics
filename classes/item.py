class Item:

    def __init__(self, type, name, level, life, attack, defense, crit_chance, crit_damage, magic_find, rarity):
        self.type = type
        self.name = name
        self.level = level
        self.life = life
        self.attack = attack
        self.defense = defense
        self.crit_chance = crit_chance
        self.crit_damage = crit_damage
        self.magic_find = magic_find
        self.rarity = rarity
